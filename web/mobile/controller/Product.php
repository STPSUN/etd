    /**
     * 个人理财收益
     */
    public function income()
    {
        $userProductM = new \web\api\model\UserProduct();
        $productM = new \addons\financing\model\Product();
        $balanceM = new \addons\member\model\Balance();
        $recordM = new \addons\member\model\TradingRecord();
        $data = $userProductM->where('release_time', '>=', NOW_DATETIME)->select();
        foreach ($data as $v)
        {
            $product = $productM->getDetail($v['product_id']);
            if(empty($product))
                continue;
            $rate = bcdiv($product['rate'],$product['duration'],2);
            $rate = bcdiv($rate,100,6);

            $num = bcmul($v['amount'],$rate);
            $balanceM->startTrans();
            $balance = $balanceM->getBalanceByCoinID($v['user_id'],$this->ETD_ID);
            try
            {
                //更新余额
                $balanceM->updateBalance($v['user_id'],$num,$this->ETD_ID,true);
                //添加交易记录
                $after_amount = $balance['amount'] + $num;
                $recordM->addRecord($v['user_id'],$this->ETD_ID,$num,$balance['amount'],$after_amount,11,1);

                $balanceM->commit();
            }catch (\Exception $e)
            {
                $balanceM->rollback();
                return $this->failData($e->getMessage());
            }
        }
    }

    /**
     * 获取直推人数
     */
    private function getDirectRecommendNum($user_id)
    {
        $count = $this->userM->where('pid',$user_id)->count();

        return $count;
    }

    /**
     * 获取团队业绩
     */
    private function getTeamAmount($id,&$result=array())
    {
        $data = $this->userM->where('pid',$id)->select();
        foreach ($data as $v)
        {
            $temp = array(
                'username' => $v['username'],
                'amount'    => $this->recordM->where(['user_id' => $v['id'], 'type' => 4])->sum('amount'),
                'user_id'   => $v['id'],
            );
            $result[] = $temp;

            $users = $this->userM->where('pid',$v['id'])->select();
            if(!empty($users))
            {
                $this->getTeamAmount($v['id'],$result);
            }
        }

        return $result;
    }

    /**
     * 更新团队社区
     */
    private function updateTeamAward()
    {

        $user_data = $this->userM->select();

        $this->memberTeamM->startTrans();
        try
        {
            foreach ($user_data as $u)
            {
                $team_amount_data = $this->getTeamAmount($u['id']);
                $amount = 0;
                foreach ($team_amount_data as $v)
                {
                    $amount += $v['amount'];
                }

                $member_team = $this->memberTeamM->where('user_id',$u['id'])->find();
                $member_team['user_id'] = $u['id'];
                $member_team['recommend_num'] = $this->getDirectRecommendNum($u['id']);
                $member_team['amount'] = $amount;
                $this->memberTeamM->save($member_team);
            }

            $this->memberTeamM->commit();
        }catch (\Exception $e)
        {
            $this->memberTeamM->rollback();
            return $this->failData($e->getMessage());
        }
    }

    /**
     * 团队理财收益
     */
    public function teamIncome()
    {
        $this->updateTeamAward();

        $level_one = $this->memberTeamM->field('user_id,amount')
            ->where('recommend_num','>=',8)
            ->where('recommend_num','<',10)
            ->where('amount','>=',300000)
            ->where('amount','<',1000000)
            ->select();

        $level_two = $this->memberTeamM->field('user_id,amount')
            ->where('recommend_num','>=',10)
            ->where('recommend_num','<',12)
            ->where('amount','>=',1000000)
            ->where('amount','<',8000000)
            ->select();

        $level_three = $this->memberTeamM->field('user_id,amount')
            ->where('recommend_num','>=',12)
            ->where('amount','>=',8000000)
            ->select();

        $this->memberTeamM->startTrans();
        try
        {
            $this->addTeamIncome($level_one,0.03);
            $this->addTeamIncome($level_two,0.02);
            $this->addTeamIncome($level_three,0.01);

            $this->memberTeamM->commit();
        }catch (\Exception $e)
        {
            $this->memberTeamM->rollback();
            return $this->failData($e->getMessage());
        }

    }

    /**
     * 团队收益发放
     * @param $data
     * @param $num
     */
    private function addTeamIncome($data,$rate)
    {
        foreach ($data as $v)
        {
            //最大收益
            $max_num = bcmul($v['amount'],$rate,8);
            //用户理财数量
            $user_amount = $this->recordM->where(['user_id' => $v['user_id'], 'type' => 4])->sum('amount');
            //可得收益数量
            $num = ($user_amount >= $max_num) ? $max_num : $user_amount;
            $balance = $this->balanceM->getBalanceByCoinID($v['user_id'],$this->ETD_ID);

            //更新余额
            $this->balanceM->updateBalance($v['user_id'],$num,$this->ETD_ID,true);
            //添加交易记录
            $after_amount = $balance['amount'] + $num;
            $this->recordM->addRecord($v['user_id'],$this->ETD_ID,$num,$balance['amount'],$after_amount,12,1);
        }
    }
